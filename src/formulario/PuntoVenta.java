    private void btn_finalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_finalizarActionPerformed
        int id_fact = Integer.parseInt(lbl_no_f.getText());

        String cli = txt_cliente_f.getText();
        String empleado = txt_vendedor_f.getText();
        String RNC = txt_rnc_f.getText();
        String total = txt_total_f.getText();

        PreparedStatement psf;
        String query = "INSERT INTO `factura`(`RNC`,`nombre_cliente`,`nombre_empleado`,`Total`) VALUES (?,?,?,?)";
        try {
            psf = MyConnection.getConnection().prepareStatement(query);

            psf.setString(1, RNC);
            psf.setString(2, cli);
            psf.setString(3, empleado);
            psf.setString(4, total);

            if (psf.executeUpdate() > 0) {

                PreparedStatement ps;
                for (int i = 0; i < tbl_detalle.getRowCount(); i++) {

                    String nombre_ = (tbl_detalle.getValueAt(i, 1).toString());
                    int precio_unit = Integer.parseInt(tbl_detalle.getValueAt(i, 2).toString());
                    int cant = Integer.parseInt(tbl_detalle.getValueAt(i, 3).toString());
                    int importe = Integer.parseInt(tbl_detalle.getValueAt(i, 4).toString());
                    String query_ = "INSERT INTO `detalle_factura`(`id_factura`,`nombre_producto`,`precio_unitario`,`cantidad`,`importe`) VALUES (?,?,?,?,?)";
                    try {
                        ps = MyConnection.getConnection().prepareStatement(query_);

                        ps.setInt(1, id_fact);
                        ps.setString(2, nombre_);
                        ps.setInt(3, precio_unit);
                        ps.setInt(4, cant);
                        ps.setInt(5, importe);

                        if (ps.executeUpdate() > 0) {

                        }
                        imprimir();
                    } catch (SQLException ex) {
                        Logger.getLogger(PuntoVenta.class.getName()).log(Level.SEVERE, null, ex);
                        JOptionPane.showMessageDialog(null, "error: " + ex);
                    }

                }
                JOptionPane.showMessageDialog(null, "Factura Guardada.");
                LimpiarDatosProdFact();
                LimpiarDatosProd();
                Mostrardatos("");
                generarIdFact();
                SetFacturaID();

            }

        } catch (SQLException ex) {
            Logger.getLogger(PuntoVenta.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "error " + ex);
        }
    }//GEN-LAST:event_btn_finalizarActionPerformed

