    final void generarIdFact() {
        String serie;
        serie = SetIdFact();
        if (serie == null) {

            lbl_no_f.setText("0");

        } else {
            int incrementar = Integer.parseInt(serie);

            lbl_no_f.setText(String.valueOf(incrementar));
        }

    }

    public String SetIdFact() {
        String serie = "";
        String sql = "SELECT MAX(idFactura) FROM factura";

        try {

            MyConnection cc = new MyConnection();
            Connection con = MyConnection.getConnection();
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs;
            rs = ps.executeQuery();

            while (rs.next()) {
                serie = rs.getString(1);

            }

        } catch (SQLException ex) {
            Logger.getLogger(PuntoVenta.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Error: " + ex);
        }

        return serie;
    }

