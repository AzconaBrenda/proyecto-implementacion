    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        PreparedStatement ps;
        ResultSet rs;

        String usuario = usuariofld.getText();
        String clave = String.valueOf(clavefld.getPassword());

        String query_adm = "SELECT * FROM `controlusuario` WHERE `correo` =? AND `clave` =?";

        try {
            ps = MyConnection.getConnection().prepareStatement(query_adm);

            ps.setString(1, usuario);
            ps.setString(2, clave);

            rs = ps.executeQuery();
            if (rs.next()) {
                try {
                    if (rs.getInt(5) == 1) {

                        ControlProductos s = new ControlProductos();
                        s.setVisible(true);
                        s.pack();
                        s.setLocationRelativeTo(null);
                        String nom = rs.getString("usuario");
                        ControlProductos.nombre = nom;

                        this.dispose();

                    } else if (rs.getInt(5) == 2) {
                        PuntoVenta mf = new PuntoVenta();
                        mf.setVisible(true);
                        mf.pack();  
                        mf.setLocationRelativeTo(null);
                        String nom_e = rs.getString("usuario");
                        PuntoVenta.nombre = nom_e;

                        this.dispose();

                    } else {

                    }
                } catch (SQLException ex) {
                    Logger.getLogger(LoginCronopio.class.getName()).log(Level.SEVERE, null, ex);
                }

            } else {
                JOptionPane.showMessageDialog(null, "Usuario o Clave incorrectos", "Fallo en Acesso", 2);
            }

        } catch (SQLException ex) {
            Logger.getLogger(LoginCronopio.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btn_loginActionPerformed

