 public final void Mostrardatos(String valor){
            //Funcion para llenar la jtable de Usuarios desde la BD
            cronopioproject.MyConnection cc=new cronopioproject.MyConnection();
            Connection cn=cronopioproject.MyConnection.getConnection();
            RefrescarTabla();

            usuarios.addColumn("id_usuario");
            usuarios.addColumn("usuario");
            usuarios.addColumn("correo");
            usuarios.addColumn("clave");
            usuarios.addColumn("id_rol");
           

            this.controlusuario.setModel(usuarios);
            String sql;
            if (valor.equals(""))
            {
            sql="SELECT * FROM controlusuario";
            }else{
            sql="SELECT * FROM controlusuario WHERE idusuario='"+valor+"'";
            } 

            String []datos=new String [5];
            try{
            Statement st=cn.createStatement();
            ResultSet rs=st.executeQuery(sql);
            while(rs.next()){
            datos[0]=rs.getString(1);
            datos[1]=rs.getString(2);
            datos[2]=rs.getString(3);
            datos[3]=rs.getString(4);   
            datos[4]=rs.getString(5); 
            
         
       

            usuarios.addRow(datos);
          
            controlusuario.setModel(usuarios);
            }
           }catch(SQLException ex){
            Logger.getLogger(ControlUsuario.class.getName()).log(Level.SEVERE,null,ex);
            JOptionPane.showMessageDialog(null, "error "+ex);
            }
        }

