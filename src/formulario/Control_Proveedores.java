public final void Mostrardatos(String valor){
            //Funcion para llenar la jtable de Usuarios desde la BD
            MyConnection cc=new MyConnection();
            Connection cn= MyConnection.getConnection();
            RefrescarTabla();
            Proveedor.addColumn("IdProveedores");
            Proveedor.addColumn("RNC");
            Proveedor.addColumn("Nombre");
            Proveedor.addColumn("Telefono");
            Proveedor.addColumn("Direccion");

            this.Proveedor_tbl.setModel(Proveedor);
            String sql = null;
            if (valor.equals(""))
            {
            sql="SELECT * FROM proveedor";
            }
         

            String []datos=new String [5];
            try{
            Statement st=cn.createStatement();
            ResultSet rs=st.executeQuery(sql);
            while(rs.next()){
            datos[0]=rs.getString(1);
            datos[1]=rs.getString(2);
            datos[2]=rs.getString(3);
            datos[3]=rs.getString(4);
            datos[4]=rs.getString(5);
       
            Proveedor.addRow(datos);
            }
            Proveedor_tbl.setModel(Proveedor);
            }catch(SQLException ex){
            Logger.getLogger(Control_Proveedores.class.getName()).log(Level.SEVERE,null,ex);
            JOptionPane.showMessageDialog(null, "error "+ex);
            }
        }
public void RefrescarTabla(){
        //Funcion encargada de Refrescar la tabla utilizando Revalidate
        try {
        Proveedor.setColumnCount(0);
        Proveedor.setRowCount(0);
        Proveedor_tbl.revalidate();
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, "error "+ex);
        }
}
public boolean RevisarProveedor(String proveedor) {
        PreparedStatement ps;
        ResultSet rs;
        boolean checkUser = false;
        String query = "Select * from 'proveedor' where 'nombre' = ?";
        
        try {
            ps = MyConnection.getConnection().prepareStatement(query);
            ps.setString(1, proveedor);
            rs = ps.executeQuery();
            if (rs.next()){
                checkUser = true;
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error" + ex);
        }
        return checkUser;
    }
