    private void guardar_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardar_btnActionPerformed
        // TODO add your handling code here:
        String RNC = RNC_txt.getText();
        String Nom = Nombre_txt.getText();
        String Tel = Telefono_txt.getText();
        String Dire = Direccion_txt.getText();
        
        if(RNC.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Agrega un Nombre del Proveedor");
        }
        
        else if(Nom.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Agrega nombre del proveedor");
        }      
        else if(RevisarProveedor(RNC))
        {
            JOptionPane.showMessageDialog(null, "Este Producto ya existe");
        }
        else{
        PreparedStatement ps;
        String query = "INSERT INTO `proveedor`(`RNC`, `Nombre`, `Direccion`, `Telefono`) VALUES (?,?,?,?)";
        try {
            ps = MyConnection.getConnection().prepareStatement(query);
            
            ps.setString(1, RNC);
            ps.setString(2, Nom);
            ps.setString(3, Tel);
            ps.setString(4, Dire);
            
            if(ps.executeUpdate() > 0)
            {
                JOptionPane.showMessageDialog(null, "Nuevo Producto Agregado");
                Limpiar();
                Mostrardatos("");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Control_Proveedores.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "error "+ex);
            }
        }
    }//GEN-LAST:event_guardar_btnActionPerformed

